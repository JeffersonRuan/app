<template>
    <div :class="baseClass">
        <slot />
    </div>
</template>

<script>
export default {
    props: ['variant'],
    computed: {
        baseClass() {
            return [
                'alert',
                this.variant ? `alert-${this.variant}` : ''
            ]
        },
    },
}
</script>

<style scoped>
.alert {
    padding: 5px;
    border-radius: 6px;
    color: gray;
    background: #ddd;
}

.alert-success {
    background: #42b983;
    color: white;
}

.alert-danger {
    background-color: red;
    color: white;
}
</style>

